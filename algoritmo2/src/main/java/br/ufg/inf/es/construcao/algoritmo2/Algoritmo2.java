package br.ufg.inf.es.construcao.algoritmo2;

/**
 * Calculo de Paridade.
 *
 * @author eric
 */
public class Algoritmo2 {

    /**
     * Verifica se um numero e par.
     *
     * @param n Numero cuja paridade sera verificada.
     * @return true para par, false para impar.
     */
    public static boolean paridade(int n) {
        return n % 2 == 0;
    }
}
